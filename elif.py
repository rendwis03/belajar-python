menu_pilihan = input ("Silahkan memilih satu angka 1-3 :")
    
if menu_pilihan == "1" : 
    print ("Kamu memilih nomer 1")
elif menu_pilihan == "2" : 
    print ("Kamu memilih nomer 2")
elif menu_pilihan == "3" :
    print ("Kamu memilih nomer 3")
else :
    print ("Kamu tidak memilih nomer 1-3")

    